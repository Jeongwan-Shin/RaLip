"""
Radar-Text pair dataset for contrastive learning.

Each sample pairs a single radar point-cloud frame with one of 5 text
descriptions generated by GPT for the corresponding action segment.

Data sources supported:
  - mmBody : {mmbody_dir}/{mode}/{segment}/frame_{i}.npz
  - mmFI   : {mmfi_dir}/{subject}/{global_idx}.npz
  - mRI    : {mri_dir}/{subject_num:02d}/{frame_idx}.npz
"""

from __future__ import annotations

import json
import os
import random
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset



# ---------------------------------------------------------------------------
# Feature pre-processing (mirrors pre_training/utils/dataloader.py)
# ---------------------------------------------------------------------------

def remove_zero_padded_points(pc: np.ndarray) -> np.ndarray:
    mask = ~np.all(pc == 0, axis=1)
    return pc[mask]


def apply_feature_norm(points: np.ndarray, mode: str) -> np.ndarray:
    if mode == "per_sample_zscore":
        xyz = points[:, :3]
        mu = xyz.mean(axis=0, keepdims=True)
        std = np.maximum(xyz.std(axis=0, keepdims=True), 1e-6)
        points = points.copy()
        points[:, :3] = (xyz - mu) / std
    return points


# ---------------------------------------------------------------------------
# Entry data-class
# ---------------------------------------------------------------------------

@dataclass
class ActionEntry:
    key: str
    source: str          # mmbody | mmfi | mri
    mode: str            # train | test (dataset split, NOT our train/test)
    segment: str
    frame_start: int
    frame_end: int
    action: str
    body_part: Dict[str, Any]
    sentences: List[str]


# ---------------------------------------------------------------------------
# Build entries from mm_actions.json + mm_actions_text.jsonl
# ---------------------------------------------------------------------------

def _action_key(item: Dict[str, Any]) -> str:
    info = item.get("info") or {}
    frames = item.get("frames") or {}
    segment = info.get("segment", "unknown_segment")
    start = frames.get("start", "na")
    end = frames.get("end", "na")
    action = item.get("action", "unknown_action")
    return f"{segment}:{start}-{end}:{action}"


def build_entries(
    actions_json_path: str,
    actions_text_jsonl_path: str,
) -> List[ActionEntry]:
    """Join mm_actions.json (full metadata) with mm_actions_text.jsonl (sentences)."""

    # Load metadata
    with open(actions_json_path, "r", encoding="utf-8") as f:
        raw_items = json.load(f)

    meta_by_key: Dict[str, Dict] = {}
    for item in raw_items:
        k = _action_key(item)
        meta_by_key[k] = item

    # Load generated text
    text_by_key: Dict[str, List[str]] = {}
    with open(actions_text_jsonl_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            obj = json.loads(line)
            text_by_key[obj["key"]] = obj["sentences"]

    # Join
    entries: List[ActionEntry] = []
    for key, sentences in text_by_key.items():
        meta = meta_by_key.get(key)
        if meta is None:
            continue
        info = meta.get("info", {})
        frames = meta.get("frames", {})
        entries.append(ActionEntry(
            key=key,
            source=info.get("from", "unknown"),
            mode=info.get("mode", "train"),
            segment=info.get("segment", ""),
            frame_start=int(frames.get("start", 0)),
            frame_end=int(frames.get("end", 0)),
            action=meta.get("action", ""),
            body_part=meta.get("body_part", {}),
            sentences=sentences,
        ))

    return entries


def split_entries(
    entries: List[ActionEntry],
    seed: int = 42,
) -> Tuple[List[ActionEntry], List[ActionEntry]]:
    """
    Split entries into train / test.

    Strategy: pick exactly 1 entry per unique action for test,
    the rest go to train.  This gives 119 test entries (one per action)
    and ~1859 train entries.
    """
    rng = random.Random(seed)

    # Group entries by action
    by_action: Dict[str, List[ActionEntry]] = {}
    for e in entries:
        by_action.setdefault(e.action, []).append(e)

    train, test = [], []
    for action, group in by_action.items():
        # Pick one random entry for test
        chosen = rng.choice(group)
        test.append(chosen)
        # Rest go to train
        for e in group:
            if e is not chosen:
                train.append(e)

    return train, test


# ---------------------------------------------------------------------------
# Dataset
# ---------------------------------------------------------------------------

class RadarTextDataset(Dataset):
    """
    Each __getitem__ returns (point_cloud: Tensor[N,5], text: str).

    During training a random frame and random sentence are sampled.
    During eval the middle frame and first sentence are used (deterministic).
    """

    MMFI_FRAMES_PER_ACTION = 297   # all mmFI recordings have 297 frames

    def __init__(
        self,
        entries: List[ActionEntry],
        mmbody_dir: str,
        mmfi_dir: str,
        mri_dir: str,
        *,
        train: bool = True,
        feature_norm: str = "per_sample_zscore",
        min_points: int = 1,
    ):
        self.entries = entries
        self.mmbody_dir = mmbody_dir
        self.mmfi_dir = mmfi_dir
        self.mri_dir = mri_dir
        self.train = train
        self.feature_norm = feature_norm
        self.min_points = min_points

        # Pre-build mmBody file index via os.walk (same as pre_training)
        self._mmbody_index: Dict[str, str] = {}  # "frame_123.npz" full path keyed by relative
        for split in ("train", "test"):
            root = os.path.join(mmbody_dir, split)
            if not os.path.isdir(root):
                continue
            for dirpath, _, fnames in os.walk(root):
                # dirpath may be .../train/sequence_0  or .../test/smoke/sequence_0
                for fn in fnames:
                    if fn.endswith(".npz"):
                        full = os.path.join(dirpath, fn)
                        # key: "{segment}/frame_{i}.npz"
                        # segment = last directory component(s) that match the entry segment
                        rel = os.path.relpath(full, os.path.join(mmbody_dir, split))
                        self._mmbody_index[rel] = full

    def __len__(self) -> int:
        return len(self.entries)

    # ---- file resolution ----

    def _resolve_file(self, entry: ActionEntry, frame_idx: int) -> str:
        if entry.source == "mmbody":
            fname = f"frame_{frame_idx}.npz"
            # Try direct: {segment}/{fname}  (works for train: "sequence_0/frame_0.npz")
            key = os.path.join(entry.segment, fname)
            if key in self._mmbody_index:
                return self._mmbody_index[key]
            # test segments like "furnished_sequence_0" → "furnished/sequence_0"
            # Replace the last "_sequence_" with "/sequence_"
            sep = "_sequence_"
            idx = entry.segment.rfind(sep)
            if idx >= 0:
                env = entry.segment[:idx]
                seq = "sequence_" + entry.segment[idx + len(sep):]
                key2 = os.path.join(env, seq, fname)
                if key2 in self._mmbody_index:
                    return self._mmbody_index[key2]
            raise FileNotFoundError(
                f"mmBody file not found: segment={entry.segment} frame={frame_idx}"
            )
        elif entry.source == "mmfi":
            # segment: "E01_S01_A02" -> subject="S01", action_num=2
            parts = entry.segment.split("_")
            subject = parts[1]                    # "S01"
            action_num = int(parts[2][1:])        # 2
            global_idx = (action_num - 1) * self.MMFI_FRAMES_PER_ACTION + frame_idx
            return os.path.join(self.mmfi_dir, subject, f"{global_idx}.npz")
        elif entry.source == "mri":
            # segment: "subject15_T pose" -> subject_num=15
            subj_str = entry.segment.split("_")[0]  # "subject15"
            subj_num = int(subj_str.replace("subject", ""))
            return os.path.join(self.mri_dir, f"{subj_num:02d}", f"{frame_idx}.npz")
        else:
            raise ValueError(f"Unknown source: {entry.source}")

    # ---- feature loading (per-dataset preprocessing) ----

    def _load_points(self, file_path: str, source: str) -> np.ndarray:
        data = np.load(file_path, allow_pickle=True, mmap_mode=None)

        if source == "mmbody":
            feature = data["feature"].copy()[:, :5]
        elif source == "mmfi":
            feature = data["feature"].copy()
            feature = feature[:, [1, 0, 2, 3, 4]]   # swap x, y
        elif source == "mri":
            feature = data["feature"].copy().reshape(-1, 5)
        else:
            raise ValueError(f"Unknown source: {source}")

        feature = remove_zero_padded_points(feature)
        if feature.shape[0] == 0:
            feature = np.zeros((1, 5), dtype=np.float32)

        feature = apply_feature_norm(feature, self.feature_norm)
        return feature.astype(np.float32)

    # ---- __getitem__ ----

    def _resolve_file_safe(self, entry: ActionEntry, frame_idx: int) -> Optional[str]:
        """Resolve file path, return None if file does not exist."""
        try:
            path = self._resolve_file(entry, frame_idx)
        except FileNotFoundError:
            return None
        return path if os.path.isfile(path) else None

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, str]:
        entry = self.entries[idx]

        # Select sentence
        if self.train:
            text = random.choice(entry.sentences)
        else:
            text = entry.sentences[0]

        # Select frame — try random/middle first, then search for valid frame
        if self.train:
            frame_idx = random.randint(entry.frame_start, entry.frame_end)
        else:
            frame_idx = (entry.frame_start + entry.frame_end) // 2

        file_path = self._resolve_file_safe(entry, frame_idx)

        if file_path is None:
            # Annotation exceeds data — search for a valid frame
            for fi in range(entry.frame_end, entry.frame_start - 1, -1):
                file_path = self._resolve_file_safe(entry, fi)
                if file_path is not None:
                    break
            if file_path is None:
                file_path = self._resolve_file_safe(entry, entry.frame_start)
            if file_path is None:
                # Last resort: return dummy
                return torch.zeros((1, 5), dtype=torch.float32), text

        points = self._load_points(file_path, entry.source)
        return torch.from_numpy(points), text


# ---------------------------------------------------------------------------
# Collate
# ---------------------------------------------------------------------------

def collate_radar_text(
    batch: List[Tuple[torch.Tensor, str]],
    max_points: int = 196,
    min_points: int = 15,
) -> Tuple[torch.Tensor, torch.Tensor, List[str]]:
    """
    Returns:
      pts:   (B, max_points, 5)
      mask:  (B, max_points) bool
      texts: list[str] of length B
    """
    points_list, texts_list = zip(*batch)

    # Filter tiny point clouds
    kept = [
        (p, t) for p, t in zip(points_list, texts_list)
        if p.shape[0] >= min_points
    ]
    if not kept:
        pts = torch.zeros((0, max_points, 5), dtype=torch.float32)
        mask = torch.zeros((0, max_points), dtype=torch.bool)
        return pts, mask, []

    points_list, texts_list = zip(*kept)
    bsz = len(points_list)

    pts = torch.zeros((bsz, max_points, 5), dtype=torch.float32)
    mask = torch.zeros((bsz, max_points), dtype=torch.bool)

    for i, p in enumerate(points_list):
        n = min(int(p.shape[0]), max_points)
        pts[i, :n] = p[:n]
        mask[i, :n] = True

    return pts, mask, list(texts_list)
